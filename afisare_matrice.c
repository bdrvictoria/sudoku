#include "lib.h"

void		afisare_matrice(int mat[10][10])
{
	int i;
	int j;

	i = 1;
	while (i <= 9)
	{
		j = 1;
		while (j <= 9)
		{
			ft_putchar(mat[i][j] + '0');
			if (j <= 8)
				ft_putchar(' ');
			j++;
		}
		ft_putchar(10);
		i++;
	}
}
